package packages;

public class ArithmaticOperators {

	public static void main(String[] args) {
		
		int a=30,b=10;
		
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b)); //Quotient
		System.out.println("a%b="+(a%b)); //reminder

	}

}
