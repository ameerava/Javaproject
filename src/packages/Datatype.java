package packages;

public class Datatype {

	public static void main(String[] args) {
		//byte
		
		byte b=121;
		System.out.println("value of b="+b);
		
		short s=2345;
		System.out.println("value of short="+s);
		
		int v=345656;
		System.out.println("value of int="+v);
		
		long l=98765432120l;
		System.out.println("value of long="+l);
		
		float v1=5.76f;
		System.out.println("value of float="+v1);
		
		double d=3.4567;
		System.out.println("value of double="+d);
		
		boolean b1=10>20;
		System.out.println("value of boolean="+b1);
		
		char c='a';
		System.out.println("value of character="+c);
		
		String s1="abc";
		System.out.println("value of string="+s1);
	}

}
